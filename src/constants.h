    /**
     * @brief A 25x25 boolean matrix for O(1) adjacency checks.
     * ADJACENCY_MATRIX[from][to] is true if 'to' is a neighbor of 'from'.
     */
    using AdjacencyMatrix = std::array < std::array < bool, 25 > , 25 > ;

    /**
     * @brief Generates the adjacency matrix at compile time.
     *
     * @return The fully populated AdjacencyMatrix.
     */
    constexpr AdjacencyMatrix generate_adjacency_matrix() {
        AdjacencyMatrix matrix {}; // Initializes all to false

        for (int from = 0; from < 25; ++from) {
            for (const sq_i to: NEIGHBOURS[from]) {
                matrix[from][to] = true;
            }
        }
        return matrix;
    }

    // The global, compile-time generated adjacency matrix.
    constexpr AdjacencyMatrix ADJACENCY_MATRIX = generate_adjacency_matrix();

